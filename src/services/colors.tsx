export const cardColor = (color: string) =>{
    switch (color) {
        case 'bug':
            return "#c6d637"
        case 'dark':
            return "#5A5465"
        case 'dragon':
            return "#0B6DC3"
        case 'electric':
            return "#ffc72c"
        case 'fairy':
            return "#EC8FE6"
        case 'fighting':
            return "#CE3967"
        case 'fire':
            return "#fc854e"
        case 'flying':
            return "#89b1f7"
        case 'ghost':
            return "#7D528D"
        case 'grass':
            return "#4ec28c"
        case 'ground':
            return "#D97845"
        case 'ice':
            return "#73CEC0"
        case 'normal':
            return "#b4b6b8"
        case 'poison':
            return "#B567CE"
        case 'psychic':
            return "#FA7179"
        case 'rock':
            return "#B2746C"
        case 'steel':
            return "#5A8EA2"      
        case 'water':
            return "#5090D6"  
        default:
            return "#c7c4c4";
    }

}